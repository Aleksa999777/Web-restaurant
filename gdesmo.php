<h1>FREQUENTLY ASKED QUESTIONS</h1>

<h3>Does Sunny Street Café® provide financing for restaurant
development?</h3>

<p>
Although we do not finance franchise development, we can provide you with a list of potential lenders who will welcome your call. We are also listed on the SBA Franchise Registry, so our agreements are already compliant with SBA lending requirements.
</p>

<h3>Can I build one restaurant or do I have to commit to several
restaurants?
</h3>

<p>
We offer both multi-unit and single unit agreements dependent on a number of factors. We consider the experience of the franchise candidate, the territory to be developed and the funds available for the project. There are also certain financial incentives available for a multi-unit commitment.
</p>

<h3>
Does Sunny Street Café® purchase the real estate on behalf of the
franchisee, provide real estate leases or build-to-suit arrangements?
</h3>

<p>
Each franchisee is responsible for acquiring real estate as well as contracting for the construction of their restaurant. However, the Sunny Street Café® team will lend its expertise in the design and layout process, as well as in site selection, lease review and negotiation.
</p>

<h3>Does Sunny Street Café® have any type of territorial restrictions?</h3>

<p>
Many areas of the country are open and available for development. Some territories, however, are covered by existing Area Development Agreements. For more information regarding territory availability complete our pre-qualification form to begin your application process.
</p>

<h3>Can I purchase a development territory?</h3>

<p>
Yes. Sunny Street Café® offers multi-unit development agreements for specific geographic areas. The terms of the development agreement (including the number of units to build and the necessary time-frame within which to have them open) are determined by the characteristics of the area in question. Becoming an Area Developer within the Sunny Street Café® system represents a significant investment of personal, professional and financial resources and is often limited to only the most qualified individuals or groups.
</p>

<h3>
What kind of support can I expect from Sunny Street Café® during the
real estate and construction process?
</h3>

<p>
We will provide you with prototypical construction plans for your in-line location. We can also suggest contractors in your area and will assist you in the pre-construction review meetings. We will also provide one on site visit during the construction phase to verify equipment placement and rough-ins. Additionally, we can assist in the development of the final punch list for your location. We encourage all of our franchisees to retain trained and experienced counsel to assist them in the lease negotiation and review process, but we will provide assistance in identifying areas unique to the Sunny Street brand.
</p>

<h3>
What type of training support does Sunny Street Café® provide in
conjunction with my first restaurant opening?
</h3>

<p>
We will be on site at your restaurant throughout the opening period to ensure that you are ready to take the reins. Our ongoing support includes regular store visits by our Operations experts providing you with up to date feedback on the performance of your restaurant. The Marketing & Advertising team will assist you with your grand opening celebration, an ongoing local store marketing plan, point of sale advertising materials, social media strategies, and will keep you informed of upcoming new product promotions.
</p>

<h3>What are the fees associated with the ongoing operation of a Sunny
Street Café®?
</h3>

<p>
Sunny Street Café® has a franchisee fee of $35,000 per store with incentives for the commitment to develop multiple locations. Our franchisees pay royalties of 5.25% of gross sales and must also contribute an additional .50% of gross sales towards our advertising fund. Additionally, you must spend up to an additional 2% of gross sales on local store marketing. This is money that you spend on the promotion of your store and is directed by you and approved by us. Some restaurants also contribute to an advertising cooperative. See the Franchise Costs tab for more info.
</p>

